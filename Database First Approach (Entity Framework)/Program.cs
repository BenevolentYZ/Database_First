using Database_First_Approach_Entity_Framework.Models;
using Microsoft.EntityFrameworkCore;

namespace Database_First_Approach_Entity_Framework
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();
            builder.Services.AddDbContext<DotNetTrainingContext>(options =>
                options.UseSqlServer(
                    builder.Configuration.GetConnectionString("TrainingDB")));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
            }
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
  


    }
}